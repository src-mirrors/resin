package com.caucho.quercus.expr;

import java.io.IOException;

import com.caucho.quercus.env.Env;
import com.caucho.quercus.env.Value;

import com.caucho.quercus.parser.QuercusParser;

import com.caucho.quercus.gen.PhpWriter;

import com.caucho.quercus.program.Statement;
import com.caucho.quercus.program.AnalyzeInfo;
import com.caucho.quercus.program.ExprStatement;
import com.caucho.quercus.Location;

/**
 * Represents an expression generated by the create routines.
 */
public class RawExpr extends Expr {
  private String _code;

  public RawExpr(Location location, String code)
  {
    super(location);
    _code = code;
  }

  public Value eval(Env env)
  {
    throw new UnsupportedOperationException();
  }

  public void generate(PhpWriter out)
    throws IOException
  {
    out.print(_code);
  }
}

